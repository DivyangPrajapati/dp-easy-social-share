    /**
     * Get social icon settings by its name.
     *
     * @param string $icon Social icon name.
     *
     * @return array {
     *     @type string $name Social icon name.
     *     @type string $icon SVG icon.
     *     @type string $url Social icon URL.
     * }
     */
    public static function get_social_icon( $icon ) {
        $svgs = self::get_social_icons();
        return isset($svgs[$icon]) ? $svgs[$icon] : [];
    }

    /**
     * Get social icon svg by its name.
     *
     * @param string $plateform Social icon name.
     *
     * @return string Sanitized SVG icon.
     */
    public static function get_svg_icon( $plateform ) {
        $icon = self::get_social_icon( $plateform );
        return isset($icon) && !empty($icon) ? self::senitize_svg($icon['icon']) : '';
    }

    /**
     * Get social icon share url by its name.
     *
     * @param string $plateform Social icon name.
     * @param string $url       URL to share.
     * @param string $title     Title to share.
     *
     * @return string Sanitized share URL.
     */
    public static function get_share_url( $plateform, $url = "#", $title = "" ) {
        $icon       = self::get_social_icon( $plateform );
        $url        = urlencode(esc_url($url));
        $title      = urlencode(esc_html($title));
        $icon_url   = isset($icon) && !empty($icon) ? $icon['url'] : '#';
        $share_url  = str_replace(['{url}', '{title}'], [$url, $title], $icon_url);
        return esc_url($share_url);
    }
